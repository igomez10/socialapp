import { signIn, signOut, useSession } from "next-auth/react";
import Head from "next/head";
import { api } from "~/utils/api";
import * as User from "../components/User";

// async function waits 1 second and returns "hello"
async function aw() {
  await new Promise((resolve) => setTimeout(resolve, 1000));
  return "hello";
}

export async function getServerSideProps() {
  const hello = await aw();

  // http get https://jsonplaceholder.typicode.com/users
  const usersResponse = await fetch(
    "https://jsonplaceholder.typicode.com/users"
  );

  const users: User.APIUser[] = (await usersResponse.json()) as User.APIUser[];

  const homeProps: HomeProps = {
    message: hello,
    users,
  };
  console.log(homeProps);
  return { props: homeProps };
}

type HomeProps = {
  message: string;
  users: User.APIUser[];
};

export default function Users(props: HomeProps) {
  const hello = api.example.hello.useQuery({ text: "from tRPC" });

  return (
    <>
      <Head>
        <title>Create T3 App Hello</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center bg-gradient-to-b from-[#2e026d] to-[#15162c]">
        {/* for each user show name */}
        <h1> Users</h1>
        {props.users.map((user) => (
          <div key={user.id}>
            <User.User props={{ user: user }} />
          </div>
        ))}
      </main>
    </>
  );
}

function AuthShowcase() {
  const { data: sessionData } = useSession();

  const { data: secretMessage } = api.example.getSecretMessage.useQuery(
    undefined, // no input
    { enabled: sessionData?.user !== undefined }
  );

  return (
    <div className="flex flex-col items-center justify-center gap-4">
      <p className="text-center text-2xl text-white">
        {sessionData && <span>Logged in as {sessionData.user?.name}</span>}
        {secretMessage && <span> - {secretMessage}</span>}
      </p>
      <button
        className="rounded-full bg-white/10 px-10 py-3 font-semibold text-white no-underline transition hover:bg-white/20"
        onClick={sessionData ? () => void signOut() : () => void signIn()}
      >
        {sessionData ? "Sign out" : "Sign in"}
      </button>
    </div>
  );
}
